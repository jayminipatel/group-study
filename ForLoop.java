package groupstudy;

public class ForLoop {
    public static void main(String[] args) {
        //create first 15 numbers
        for(int t=1;t<=15;t++){
            System.out.println(t);
         }
        //create table of 4
        for (int w=1;w<=14;w++){
            System.out.println("4*"+w+"="+4*w);

        }
    }
}
