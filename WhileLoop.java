package groupstudy;

public class WhileLoop {
    public static void main(String[] args) {
        // create even numbers
        int e = 12;
        while (e <= 30) {
            System.out.println(e);
            e += 2;

        }
        int o = 11;
        while (o <= 30) {
            System.out.println(o);
            o += 2;
        }
        // create table of 8
        int number = 1;
        while (number <= 20) {
            System.out.println("8 *" + number + "=" + 8 * number);
            number++;


        }
    }
}
