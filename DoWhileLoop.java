package groupstudy;

public class DoWhileLoop {
    public static void main(String[] args) {

        // create even number
        int d = 30;
        do {
            System.out.println(d);
            d += 2;
        } while (d<=45);

        // create odd number
        int h=31;
        do{
            System.out.println(h);
            h+=2;
        }while (h<=45);

        // create table of 5
        int k=1;
        do {
            System.out.println("5*"+k +"="+5*k);
            k++;
        }while (k<=18);


        }

    }

